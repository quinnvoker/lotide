/*
  function eqArrays(arrayA, arrayB):
    if arrays length are not equal
      return false
    for each element of arrayA (with iterator i):
      check if arrayA[i] !== arrayB[i]
        if so, return false
    return true
*/

const eqArrays = function(arrayA, arrayB) {
  if (arrayA.length !== arrayB.length) {
    return false;
  }
  for (let i = 0; i < arrayA.length; i++) {
    if (arrayA[i] !== arrayB[i]) {
      if (Array.isArray(arrayA[i]) && Array.isArray(arrayB[i]) && eqArrays(arrayA[i], arrayB[i])) {
        continue;
      }
      return false;
    }
  }
  return true;
};

assertEqual(eqArrays([1, 2, 3], [1, 2, 3]), true); // => should PASS

// recursive tests

assertEqual(eqArrays([[2, 3], [4]], [[2, 3], [4]]), true); // => true

assertEqual(eqArrays([[2, 3], [4]], [[2, 3], [4, 5]]), false); // => false
assertEqual(eqArrays([[2, 3], [4]], [[2, 3], 4]), false); // => false

